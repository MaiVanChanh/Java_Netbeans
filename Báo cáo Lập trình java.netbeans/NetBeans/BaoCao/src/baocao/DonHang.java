        public void loadDH()
    {
         try {
             
            tbDH.removeAll();          
            Connection connection = DuLieu.getConnection();
            if(connection==null)
                JOptionPane.showMessageDialog(null,"kết nối thất bại");
            String [] arr={"Mã đơn hàng ","Tên khách hàng","Địa chỉ khách hàng","Số Điện Thoại","Tên Sản Phẩm","Giá","Tên Nhà Sản Xuất ","Số điện thoại NSX","Ngày đặt hàng"};
            DefaultTableModel model = new DefaultTableModel(arr,0);
            String sql ="select * From DonHang";
            PreparedStatement ps = connection.prepareStatement(sql);
            ResultSet rs= ps.executeQuery();
            while(rs.next())
            {
                Vector vector = new Vector();
                vector.add(rs.getInt("IDDH"));
               vector.add(rs.getString("TenKH")); 
                vector.add(rs.getString("Diakh"));
               vector.add(rs.getString("SDTKH"));
               vector.add(rs.getString("TenSP"));
                vector.add(rs.getString("Gia"));
                vector.add(rs.getString("TenNSX"));  
                vector.add(rs.getString("TenSDTNSX"));
               vector.add(rs.getString("NgayDat"));
            
                model.addRow(vector);
            }
            tbDH.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(Test.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
