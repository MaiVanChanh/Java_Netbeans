    private void tbDHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDHMouseClicked
              try {
            int row = this.tbDH.getSelectedRow();
            String IDrow=(this.tbDH.getModel().getValueAt(row,0)).toString();
            
            String sql1="select * from DonHang where IDDH='"+IDrow+"'";
            ResultSet rst= DuLieu.Showtb(sql1);
            if(rst.next())
            {
                MaNHASX=rst.getString("IDDH");
                this.txtid.setText(rst.getString("IDDH"));
                this.txtten.setText(rst.getString("TenKH"));
                
            }
        } catch (Exception ex) {
            Logger.getLogger(Test.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tbDHMouseClicked

